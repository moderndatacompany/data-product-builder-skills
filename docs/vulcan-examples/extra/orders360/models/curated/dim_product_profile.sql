WITH product_sales AS (
  SELECT
    p.product_id,
    p.name AS product_name,
    p.category,
    p.supplier_id,
    s.name AS supplier_name,
    p.price AS current_price,
    MIN(DATE(o.order_date)) AS first_sale_date,
    MAX(DATE(o.order_date)) AS last_sale_date,
    COALESCE((CURRENT_DATE - MIN(DATE(o.order_date)))::INTEGER, 0) AS days_since_first_sale,
    COALESCE((CURRENT_DATE - MAX(DATE(o.order_date)))::INTEGER, 0) AS days_since_last_sale,
    COUNT(DISTINCT oi.order_id) AS total_orders,
    SUM(oi.quantity) AS total_quantity_sold,
    SUM(oi.quantity * oi.unit_price) AS total_revenue,
    AVG(oi.unit_price) AS avg_price_sold,
    COUNT(DISTINCT o.customer_id) AS unique_customers
  from raw.products AS p
  INNER JOIN bronze.suppliers AS s
    ON p.supplier_id = s.supplier_id
  LEFT JOIN bronze.order_items AS oi
    ON p.product_id = oi.product_id
  LEFT JOIN bronze.orders AS o
    ON oi.order_id = o.order_id
  GROUP BY p.product_id, p.name, p.category, p.supplier_id, s.name, p.price
)
SELECT
  ps.product_id,
  ps.product_name,
  ps.category,
  ps.supplier_id,
  ps.supplier_name,
  ps.current_price,
  CAST(ps.first_sale_date AS TIMESTAMP) AS first_sale_date,
  CAST(ps.last_sale_date AS TIMESTAMP) AS last_sale_date,
  ps.days_since_first_sale,
  ps.days_since_last_sale,
  COALESCE(ps.total_orders, 0) AS total_orders,
  COALESCE(ps.total_quantity_sold, 0) AS total_quantity_sold,
  ROUND(COALESCE(ps.total_revenue, 0), 2) AS total_revenue,
  ROUND(ps.total_quantity_sold::NUMERIC / NULLIF(ps.total_orders, 0), 2) AS avg_quantity_per_order,
  ROUND(COALESCE(ps.avg_price_sold, 0), 2) AS avg_price_sold,
  COALESCE(ps.unique_customers, 0) AS unique_customers,
  ROUND(ps.total_orders::NUMERIC / NULLIF(CURRENT_DATE - ps.first_sale_date, 0), 4) AS inventory_turnover_rate,
  CASE
    WHEN ps.total_orders IS NULL OR ps.total_orders = 0 THEN 'New'
    WHEN CURRENT_DATE - ps.last_sale_date > 90 THEN 'Stale'
    WHEN ps.total_revenue >= 10000 THEN 'Best Seller'
    WHEN ps.total_revenue >= 2000 THEN 'Good Performer'
    ELSE 'Slow Mover'
  END AS product_performance
FROM product_sales AS ps