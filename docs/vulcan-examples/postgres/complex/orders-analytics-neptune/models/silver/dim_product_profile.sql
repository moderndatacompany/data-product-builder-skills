WITH total_customer_count AS (
  -- Get total number of customers for penetration calculation
  SELECT COUNT(DISTINCT customer_id) AS total_customers
  FROM bronze_v1.customers
),
product_sales AS (
  SELECT
    p.product_id,
    p.name AS product_name,
    p.category,
    p.supplier_id,
    s.name AS supplier_name,
    p.price AS current_price,
    MIN(DATE(o.order_date)) AS first_sale_date,
    MAX(DATE(o.order_date)) AS last_sale_date,
    COALESCE((CURRENT_DATE - MIN(DATE(o.order_date)))::INTEGER, 0) AS days_since_first_sale,
    COALESCE((CURRENT_DATE - MAX(DATE(o.order_date)))::INTEGER, 0) AS days_since_last_sale,
    COUNT(DISTINCT oi.order_id) AS total_orders,
    SUM(oi.quantity) AS total_quantity_sold,
    SUM(oi.quantity * oi.unit_price) AS total_revenue,
    AVG(oi.unit_price) AS avg_price_sold,
    COUNT(DISTINCT o.customer_id) AS unique_customers
  FROM bronze_v1.products AS p
  INNER JOIN bronze_v1.suppliers AS s
    ON p.supplier_id = s.supplier_id
  LEFT JOIN bronze_v1.order_items AS oi
    ON p.product_id = oi.product_id
  LEFT JOIN bronze_v1.orders AS o
    ON oi.order_id = o.order_id
  GROUP BY p.product_id, p.name, p.category, p.supplier_id, s.name, p.price
),
product_with_ranks AS (
  SELECT
    ps.*,
    ROW_NUMBER() OVER (PARTITION BY ps.category ORDER BY COALESCE(ps.total_revenue, 0) DESC) AS revenue_rank
  FROM product_sales AS ps
)
SELECT
  pwr.product_id,
  pwr.product_name,
  pwr.category,
  pwr.supplier_id,
  pwr.supplier_name,
  pwr.current_price,
  CAST(pwr.first_sale_date AS TIMESTAMP) AS first_sale_date,
  CAST(pwr.last_sale_date AS TIMESTAMP) AS last_sale_date,
  pwr.days_since_first_sale,
  pwr.days_since_last_sale,
  COALESCE(pwr.total_orders, 0) AS total_orders,
  COALESCE(pwr.total_quantity_sold, 0) AS total_quantity_sold,
  ROUND(COALESCE(pwr.total_revenue, 0), 2) AS total_revenue,
  ROUND(pwr.total_quantity_sold::NUMERIC / NULLIF(pwr.total_orders, 0), 2) AS avg_quantity_per_order,
  ROUND(COALESCE(pwr.avg_price_sold, 0), 2) AS avg_price_sold,
  COALESCE(pwr.unique_customers, 0) AS unique_customers,
  -- Customer Penetration Percentage (market reach)
  ROUND(
    COALESCE(pwr.unique_customers, 0)::NUMERIC / NULLIF(tcc.total_customers, 0) * 100,
    2
  ) AS customer_penetration_pct,
  ROUND(pwr.total_orders::NUMERIC / NULLIF(CURRENT_DATE - pwr.first_sale_date, 0), 4) AS inventory_turnover_rate,
  -- Revenue Rank within Category
  pwr.revenue_rank AS revenue_rank_in_category,
  -- Product Performance Status Classification
  CASE
    WHEN pwr.total_orders IS NULL OR pwr.total_orders = 0 THEN 'New'
    WHEN CURRENT_DATE - pwr.last_sale_date > 90 THEN 'Stale'
    WHEN pwr.total_revenue >= 10000 AND pwr.days_since_last_sale <= 30 THEN 'Best Seller'
    WHEN pwr.total_revenue >= 2000 AND pwr.days_since_last_sale <= 60 THEN 'Good Performer'
    WHEN pwr.total_revenue >= 2000 AND pwr.days_since_last_sale > 60 THEN 'Declining'
    WHEN pwr.total_revenue < 2000 AND pwr.days_since_last_sale <= 60 THEN 'Slow Mover'
    ELSE 'At Risk'
  END AS product_performance
FROM product_with_ranks AS pwr
CROSS JOIN total_customer_count AS tcc

