  name silver_v1.dim_product_profile,
  kind FULL,
  cron '@daily',
  grains (product_id),
  tags ('dimension', 'aggregated', 'product', 'profile', 'silver'),
  terms ('product.profile_analytics', 'analytics.product_performance'),
  description 'Product profile dimension containing aggregated sales metrics including order frequency, revenue contribution, customer reach, and inventory turnover for product analytics and merchandising decisions',
